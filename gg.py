import math


def checkerxs2(w: int, h: int) -> print():
    """Just a function that print"""
    for x in range(w):

        y = h - x * x * h / (w * w)
        xs2 = x * x
        hmxx = h - x * x
        hdw = h / (w * w)
        xxx = x * x * h / (w * w)
        str(y)
        str(x)
        str(h)
        str(w)
        str(xs2)
        str(hmxx)
        str(xxx)
        print("ans:", "{:.2f}".format(y), "x^2:", xs2,
              "h - x^2:", hmxx, "h / (w * w):", hdw, "xxx:", xxx)
        #print("{:.2f}".format(y), "=", "50 -", x, "*", x, "* 50 / (40 * 40)")
        int(y)
        int(x)
        int(h)
